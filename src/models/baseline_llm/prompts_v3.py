def build_user_message_v3(tender_id: str, section: str, text: str) -> str:
    text = text[:6000]
    allowed = SECTION_LABEL_WHITELIST.get(section, set(LABELS))
    allowed_str = ", ".join(sorted(allowed)) if allowed else "ANY"
    return (
        f"Tender ID: {tender_id}\n"
        f"Section: {section}\n"
        f"Релевантные метки для этой секции: {allowed_str}\n\n"
        f"Текст секции:\n---\n{text}\n---\n\n"
        f"Прогони чеклист whitelist (W1-W15) для каждого кандидата. "
        f"Не флагай описания структуры, цитаты 223-ФЗ, защитные правила, "
        f"определения терминов. Верни только реальные аномалии в формате JSON."
    )
