# -*- coding: utf-8 -*-
"""Глубокое сканирование на предмет несогласований после автозамены.

Универсальный подход: для каждого переведённого слова ищем паттерны вида
"<предлог> <имен.падеж>" где должен быть другой падеж.
"""
import io
import re
import tokenize
from collections import defaultdict
from pathlib import Path

src = Path(__file__).resolve().parents[1] / "scripts/build_vkr.py"
text = src.read_text(encoding="utf-8")
tokens = list(tokenize.tokenize(io.BytesIO(text.encode("utf-8")).readline))

QUOTES = ('"""', "'''", '"', "'")
literals = []
for tok in tokens:
    if tok.type != tokenize.STRING:
        continue
    s = tok.string
    while s and s[0] in "rbfRBF":
        s = s[1:]
    for q in QUOTES:
        if s.startswith(q) and s.endswith(q) and len(s) >= 2 * len(q):
            literals.append((tok.start[0], s[len(q):-len(q)]))
            break

# Слова в им. падеже которые часто остаются после неудачной автозамены
# Каждое слово -> список предлогов после которых ОНО НЕ должно стоять в им.
SUSPICIOUS_NOMINATIVE = {
    "белый список": ["в", "на", "из", "по", "к", "у", "для", "после", "перед", "при",
                      "против", "без", "о", "об", "обо", "над", "под", "за", "с"],
    "обучающая выборка": ["в", "на", "из", "по", "к", "у", "для", "после", "перед",
                            "при", "без", "над", "под", "за", "с"],
    "вспомогательная разметка": ["в", "на", "из", "по", "к", "у", "для", "после",
                                    "перед", "при", "без", "с"],
    "исходная конфигурация": ["в", "на", "из", "по", "к", "у", "для", "после",
                                  "перед", "при", "против", "без", "о", "с"],
    "каскадная архитектура": ["в", "на", "из", "по", "к", "у", "для", "после",
                                "перед", "при", "без", "с"],
    "приоритизация": ["в", "на", "из", "по", "к", "у", "для", "после",
                          "перед", "при", "без", "с"],
    "компромисс": ["после", "до", "без"],  # требует генитива
    "ансамбль": ["после", "до"],
    "сговоры": ["после", "до"],  # сговоры на торгах допустимо
    "сговор на торгах": ["в", "на", "при", "после"],
    "перенос обучения": ["после", "до", "без", "у"],  # требует генитива
    "ложные срабатывания": ["в", "на", "из", "по", "к", "после", "при", "без"],
    "вывод модели": ["после", "до", "при", "в"],  # вывод (имя сущ.) -> вывода
    "дообучение": ["после", "до", "без", "при", "в"],
    "предобучение": ["после", "до", "без", "при", "в"],
    "конвейер": ["в", "на", "из", "по", "к", "у", "для", "после",
                    "при", "без", "с"],
    "запоминание": ["после", "при", "в"],
    "обобщение": ["после", "при", "в"],
    "развёртывание": ["после", "при", "в"],  # развёртывание -> развёртывания/развёртывании
    "приоритет": ["после", "при", "в"],
}

# Прилагательные, которые могут быть в неправильном роде
SUSPICIOUS_ADJ = [
    # дообученный/дообученная согласование с моделью (ж.р.)
    (r"\bдообученный\s+модел[ьеию]+\b", "дообученной модели"),
    (r"\bдообученное\s+модел[ьеию]+\b", "дообученной модели"),
    # отложенный/отложенная согласование с выборкой/оценкой/валидацией (ж.р.)
    (r"\bотложенный\s+(выборк[еаию]+|оценк[еаию]+|валидаци[еияю]+)", r"отложенной \1"),
    (r"\bотложенный\s+корпус[еа]?\b", "отложенный корпус"),  # м.р. — OK
    # многометочный с таксономией (ж.р.)
    (r"\bмногометочный\s+таксоном", "многометочной таксоном"),
    # передовой/передовая
    (r"\bпередовой\s+модел[ьеию]+\b", "передовой модели"),
    (r"\bпередовая\s+gpt\b", "передовая gpt"),  # OK
]

found = []

for line_num, lit in literals:
    # Проверка на NOMINATIVE после предлогов
    for word, preps in SUSPICIOUS_NOMINATIVE.items():
        for prep in preps:
            pattern = rf"\b{prep}\s+{word}\b(?!\s*\.|\s+W1)"
            for m in re.finditer(pattern, lit, re.IGNORECASE):
                # Проверка контекста (избегать имена конфигураций)
                ctx_start = max(0, m.start() - 20)
                ctx = lit[ctx_start:m.end() + 20]
                if "v1 baseline" in ctx or "v2 + 2-stage" in ctx:
                    continue
                found.append((line_num, m.group(0), word, prep, ctx))

    # Проверка прилагательных
    for pat, repl in SUSPICIOUS_ADJ:
        for m in re.finditer(pat, lit):
            found.append((line_num, m.group(0), pat, "adj", lit[max(0, m.start()-20):m.end()+20]))

if not found:
    print("ВСЁ ЧИСТО — несогласованных конструкций НЕ найдено.")
else:
    grouped = defaultdict(list)
    for entry in found:
        line, match, word, prep, ctx = entry
        key = (match, word, prep)
        grouped[key].append((line, ctx))
    print(f"Найдено {len(found)} мест, {len(grouped)} уникальных:")
    print()
    for (match, word, prep), lst in sorted(grouped.items(), key=lambda x: -len(x[1])):
        print(f"  [{len(lst)}×] {match!r}")
        if prep == "adj":
            print(f"       прилагательное-сущ.: {word!r}")
        else:
            print(f"       (предлог '{prep}' + им.п. '{word}')")
        for line, ctx in lst[:3]:
            print(f"       стр. {line}: ...{ctx.strip()!r}...")
        if len(lst) > 3:
            print(f"       ...и ещё {len(lst)-3}")
        print()
