found = []
for line_num, lit in literals_with_lines:
    for pattern, repl in SUSPICIOUS_PATTERNS:
        for m in re.finditer(pattern, lit):
            found.append((line_num, pattern, m.group(0), repl))

# Группируем находки
grouped = defaultdict(list)
for line, pat, match, repl in found:
    grouped[match].append((line, repl))

if not found:
    print("ВСЁ ЧИСТО — несогласований не найдено!")
else:
    print(f"НАЙДЕНО {len(found)} мест несогласований:")
    print()
    for match, occurrences in sorted(grouped.items(), key=lambda x: -len(x[1])):
        first = occurrences[0]
        repl_sample = first[1]
        if callable(repl_sample):
            repl_sample = "(контекстная)"
        print(f"  [{len(occurrences)}×] {match!r}")
        if isinstance(repl_sample, str):
            print(f"       -> {repl_sample!r}")
        for line, _ in occurrences[:5]:
            print(f"       стр. {line}")
        if len(occurrences) > 5:
            print(f"       ...и ещё {len(occurrences)-5}")
        print()
