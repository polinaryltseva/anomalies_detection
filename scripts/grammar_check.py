# -*- coding: utf-8 -*-
"""Системная проверка несогласованных падежей и родов после автозамены."""
import io
import re
import tokenize
from pathlib import Path

src = Path(__file__).resolve().parents[1] / "scripts/build_vkr.py"
text = src.read_text(encoding="utf-8")
tokens = list(tokenize.tokenize(io.BytesIO(text.encode("utf-8")).readline))

# Извлекаем содержимое строковых литералов с указанием строк
QUOTES = ('"""', "'''", '"', "'")
literals_with_lines = []  # [(line_num, content)]
for tok in tokens:
    if tok.type != tokenize.STRING:
        continue
    s = tok.string
    while s and s[0] in "rbfRBF":
        s = s[1:]
    for q in QUOTES:
        if s.startswith(q) and s.endswith(q) and len(s) >= 2 * len(q):
            content = s[len(q):-len(q)]
            literals_with_lines.append((tok.start[0], content))
            break

# Подозрительные паттерны несогласований
SUSPICIOUS = [
    # Слова в им. падеже после предлогов, требующих другого падежа
    (r"\bтаксономии\s+красные\s+флаги\b", "таксономии красных флагов"),
    (r"\bтаксономия\s+красные\s+флаги\b", "таксономия красных флагов"),
    (r"\bтаксономиями\s+красные\s+флаги\b", "таксономиями красных флагов"),
    (r"\bтаксономию\s+красные\s+флаги\b", "таксономию красных флагов"),
    (r"\bтекстовых\s+красные\s+флаги\b", "текстовых красных флагов"),
    (r"\bоперациональных\s+красные\s+флаги\b", "операциональных красных флагов"),
    (r"\bкатегорий\s+красные\s+флаги\b", "категорий красных флагов"),
    (r"\bна основе EU/OECD\s+красные\s+флаги\b", "на основе красных флагов EU/OECD"),
    (r"\bEU/OECD\s+красные\s+флаги\b", "красных флагов EU/OECD"),
    (r"\bмеждународной\s+таксономии\s+красные\s+флаги\b", "международной таксономии красных флагов"),
    (r"\bмеждународной\s+таксонией\s+красные\s+флаги\b", "международной таксонией красных флагов"),
    (r"\bтаксономии\s+красные\s+флаги\b", "таксономии красных флагов"),
    (r"\bпереноса\s+таксономии\s+красные\s+флаги\b", "переноса таксономии красных флагов"),
    (r"\bперенос таксономии\s+красные\s+флаги\b", "перенос таксономии красных флагов"),

    # белый список после предлогов
    (r"\bв виде\s+белый\s+список\b", "в виде белого списка"),
    (r"\bкатегорий\s+белый\s+список\b", "категорий белого списка"),
    (r"\bпаттерн[уыовм]+\s+белый\s+список\b", "паттернам белого списка"),

    # обучающая выборка после предлогов
    (r"\bв\s+обучающая\s+выборка\b", "в обучающую выборку"),
    (r"\bна\s+обучающая\s+выборка\b", "на обучающей выборке"),
    (r"\bиз\s+обучающая\s+выборка\b", "из обучающей выборки"),

    # дообученный модели
    (r"\bдообученный\s+модели\b", "дообученной модели"),

    # отложенный после предлогов с разными словами
    (r"\bоценка\s+на\s+отложенный\s+(\w+)", r"оценка на отложенной \1"),
    (r"\bна\s+отложенный\s+валидаци", "на отложенной валидаци"),
    (r"\bв\s+отложенный\s+(\w+)\b", r"в отложенной \1"),

    # вывод модели в косвенных падежах
    (r"\bстоимость\s+вывод\s+модели\b", "стоимость вывода модели"),

    # производство в косвенных падежах
    (r"\bдля\s+производства-сценари[ияеемю]+", "для производственного сценария"),

    # вспомогательная разметка
    (r"\bочистки\s+вспомогательная\s+разметка\b", "очистки вспомогательной разметки"),

    # Дублирующие предлоги
    (r"\bпри\s+при\s+", "при "),
    (r"\bна\s+на\s+", "на "),
    (r"\bв\s+в\s+", "в "),
    (r"\bи\s+и\s+", "и "),
]

found = []
for line_num, lit in literals_with_lines:
    for pattern, repl in SUSPICIOUS:
        if re.search(pattern, lit):
            for m in re.finditer(pattern, lit):
                found.append((line_num, pattern, m.group(0), repl))

if not found:
    print("Подозрительных несогласований НЕ найдено.")
else:
    print(f"Найдено {len(found)} мест несогласований:")
    for line, pat, match, repl in found[:60]:
        print(f"  стр. {line}: {match[:80]!r}")

# Дополнительно: найти все вхождения "красные флаги" с контекстом ±20 chars
print()
print("Все вхождения 'красные флаги' с контекстом:")
for line_num, lit in literals_with_lines:
    for m in re.finditer(r"красные\s+флаги", lit):
        start = max(0, m.start() - 30)
        end = min(len(lit), m.end() + 30)
        ctx = lit[start:end].replace("\n", " ")
        print(f"  стр. {line_num}: ...{ctx}...")
